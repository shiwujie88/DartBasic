/*算术运算符*/
void main(){
  int a = 10;
  int b = 2;

  //算符运算符
  print(a + b); //加 -> 12
  print(a - b); //减 -> 8
  print(a * b); //乘 -> 20
  print(a / b); //除 -> 5.0
  print(a ~/ b); //取整 -> 5
  print(a % b); //取余 -> 0

 //自增自减运算符
  print(a++);  //先运算再累加 -> 10
  print(++a);  //先累加再运算 -> 12
  print(a--); // -> 12
  print(--a); // -> 11


  
  
  
  
  
}